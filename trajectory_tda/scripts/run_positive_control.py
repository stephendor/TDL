# Research context: TDA-Research/03-Papers/P01-A/_project.md
# Purpose: Positive control for the W₂ Markov-1 permutation test.
#   Generates one synthetic trajectory cloud from the fitted global Markov-1
#   chain, then runs the standard W₂ Markov-1 null test on that cloud.
#   Expected result: p ≈ 0.5 for both H₀ and H₁ — the null model reproduces
#   the topology of data it generated.  Addresses reviewer §10.2.

from __future__ import annotations

import argparse
import json
import logging
import time
from pathlib import Path
from typing import Any

import numpy as np

from trajectory_tda.scripts.run_wasserstein_battery import load_checkpoint
from trajectory_tda.topology.permutation_nulls import (
    _markov_shuffle,
    permutation_test_trajectories,
)

logger = logging.getLogger(__name__)


def _convert_numpy(obj: object) -> object:
    """Recursively convert numpy types for JSON serialisation."""
    if isinstance(obj, np.ndarray):
        return obj.tolist()
    if isinstance(obj, np.integer):
        return int(obj)
    if isinstance(obj, np.floating):
        return float(obj)
    if isinstance(obj, dict):
        return {str(k): _convert_numpy(v) for k, v in obj.items()}
    if isinstance(obj, (list, tuple)):
        return [_convert_numpy(v) for v in obj]
    return obj


def run_positive_control(
    checkpoint_dir: Path,
    n_permutations: int = 100,
    n_landmarks: int = 5000,
    seed: int = 42,
    output_path: Path | None = None,
) -> dict[str, Any]:
    """Run positive-control W₂ Markov-1 test.

    Generates one synthetic Markov-1 cloud from the real trajectories (using
    ``seed``), then runs the standard Markov-1 permutation test on that cloud
    (null draws use ``seed + 1``).  Because the observed cloud was itself
    generated by the null model, the test is expected to fail to reject
    (p ≈ 0.5).

    Args:
        checkpoint_dir: Path to a pipeline checkpoint with embeddings.npy and
            01_trajectories_sequences.json.
        n_permutations: Number of null permutations (obs-null comparisons).
        n_landmarks: Landmark count for VR persistence computation.
        seed: RNG seed for generating the synthetic observed cloud.  Null draws
            use seed + 1 to avoid correlation.
        output_path: Where to write the JSON result.  If None, returns dict only.

    Returns:
        Results dict with keys H0, H1, positive_control metadata.
    """
    _, trajectories, embed_kwargs = load_checkpoint(checkpoint_dir)
    n_traj = len(trajectories)

    logger.info(
        "Positive control: generating Markov-1 synthetic cloud "
        "(n=%d trajectories, seed=%d)",
        n_traj,
        seed,
    )
    t0 = time.time()

    # Step 1 — generate the "observed" synthetic cloud from Markov-1
    rng_obs = np.random.RandomState(seed)
    synthetic_embeddings = _markov_shuffle(
        trajectories,
        rng_obs,
        markov_order=1,
        embed_kwargs=embed_kwargs,
    )
    logger.info(
        "Synthetic cloud generated: shape %s (%.1fs)",
        synthetic_embeddings.shape,
        time.time() - t0,
    )

    # Step 2 — run the standard Markov-1 W₂ test on the synthetic cloud.
    # Null draws also come from the Markov-1 model fitted on the real
    # trajectories — so null and "observed" come from the same generative
    # process.  Expected: p ≈ 0.5.
    logger.info(
        "Running Markov-1 W₂ permutation test on synthetic cloud "
        "(n_perms=%d, L=%d, seed=%d)",
        n_permutations,
        n_landmarks,
        seed + 1,
    )
    result: dict[str, Any] = permutation_test_trajectories(
        embeddings=synthetic_embeddings,
        trajectories=trajectories,
        null_type="markov",
        n_permutations=n_permutations,
        max_dim=1,
        n_landmarks=n_landmarks,
        statistic="wasserstein",
        markov_order=1,
        n_jobs=1,
        seed=seed + 1,
        embed_kwargs=embed_kwargs,
    )

    elapsed = time.time() - t0
    result["positive_control"] = True
    result["synthetic_cloud_seed"] = seed
    result["null_draws_seed"] = seed + 1
    result["n_landmarks"] = n_landmarks
    result["n_permutations"] = n_permutations
    result["checkpoint_dir"] = str(checkpoint_dir)
    result["elapsed_seconds"] = elapsed

    logger.info(
        "Positive control complete (%.1fs): "
        "H0 p=%.4f, H1 p=%.4f  (expected ≈ 0.5 for both)",
        elapsed,
        result.get("H0", {}).get("p_value", float("nan")),
        result.get("H1", {}).get("p_value", float("nan")),
    )

    if output_path is not None:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, "w") as f:
            json.dump(_convert_numpy(result), f, indent=2)
        logger.info("Saved to %s", output_path)

    return result


def main() -> None:
    parser = argparse.ArgumentParser(
        description=(
            "Positive control: run W₂ Markov-1 test on a Markov-generated cloud. "
            "Expected p ≈ 0.5 for both H₀ and H₁."
        )
    )
    parser.add_argument(
        "--checkpoint-dir",
        type=str,
        required=True,
        help="Path to pipeline checkpoint directory (contains embeddings.npy).",
    )
    parser.add_argument("--n-perms", type=int, default=100)
    parser.add_argument("--landmarks", type=int, default=5000)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument(
        "--output",
        type=str,
        required=True,
        help="Output JSON path.",
    )

    args = parser.parse_args()

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s %(name)s %(levelname)s %(message)s",
    )

    run_positive_control(
        checkpoint_dir=Path(args.checkpoint_dir),
        n_permutations=args.n_perms,
        n_landmarks=args.landmarks,
        seed=args.seed,
        output_path=Path(args.output),
    )


if __name__ == "__main__":
    main()
